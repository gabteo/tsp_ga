class cromossomo(object):
    individual = []
    def __init__(self):
        return self.individual

    def __init__(self, size):
        for i in range(size):
            self.individual[i] = ""
        return self.individual

    def __init__(self, cidades):
        for i in range(len(cidades)):
            self.individual[i] = cidades[i]
        return self.individual





